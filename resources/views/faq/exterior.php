            <div class="col-md-8">
                <div class="questions">
                    <h2>Preguntas Frecuentes</h2>
                    <div class="question">
                        <div class="question-heading">
                            ¿Qué puedo encontrar en el portal ChileAtiende en el Exterior?
                        </div>
                        <div class="answer">
                            Podrá obtener información y orientación sobre los programas y beneficios destinados a los chilenos que viajan, residen o se encuentran temporalmente en el extranjero.
                        </div>
                    </div>
                    <div class="question">
                        <div class="question-heading">
                            ¿Cómo puedo contactarme con ChileAtiende en el Exterior?
                        </div>
                        <div class="answer">
                            Para resolver sus dudas sobre los beneficios y servicios entregados por el Estado, podrá visita el portal <a href="/?skin=exterior">www.chileatiende.gob.cl/?skin=exterior</a>. Si aún así necesita realizar consultas, reclamos o sugerencias, podrá hacerlo a través del formulario de contacto, disponible en este mismo portal.
                        </div>
                    </div>
                    <div class="question">
                        <div class="question-heading">
                            ¿Qué información puedo encontrar en las fichas de trámites publicadas en el portal ChileAtiende en el Exterior?
                        </div>
                        <div class="answer">
                            En cada ficha obtendrá información sobre la descripción del trámite, beneficiarios (incluye los requisitos que deben cumplir), documentos requeridos, procedimiento para solicitar o acceder a los beneficios y servicios (paso a paso), costo (puede ser gratuito o tener costo asociado), vigencia y marco legal.
                        </div>
                    </div>
                    <div class="question">
                        <div class="question-heading">
                            ¿El servicio que entrega el portal ChileAtiende en el Exterior es gratuito?
                        </div>
                        <div class="answer">
                            Sí. Sin embargo, debe tener en consideración que existen algunos trámites que tienen costo, según lo definido por las instituciones responsables de cada proceso. Este dato se encuentra disponible en las fichas publicadas en el portal, bajo el título costo del trámite.
                        </div>
                    </div>
                    <div class="question">
                        <div class="question-heading">
                            ¿Qué son los consulados y para qué sirven?
                        </div>
                        <div class="answer">
                            Los consulados son órganos de la administración pública de un Estado en el territorio de otro, con el objetivo principal de asistir y proteger a sus compatriotas.
                        </div>
                    </div>
                    <div class="question">
                        <div class="question-heading">
                            ¿Qué trámites se realizan en los consulados?
                        </div>
                        <div class="answer">
                            Los trámites que podrá realizar en el consulado dependerán del tipo de beneficiario. En las fichas publicadas podrá encontrar información detallada de los trámites disponibles en los consulados. Entre los trámites que destacan, se encuentra la obtención de documentos de identificación (cédula de identidad y pasaporte) para chilenos que se encuentren en el exterior, la solicitud de visa para visitar Chile en caso de extranjeros y en el caso de empresas la obtención de pasavante que autoriza a buques a navegar con bandera chilena.
                        </div>
                    </div>
                </div>
