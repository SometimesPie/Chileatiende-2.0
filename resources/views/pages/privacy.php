				<h1>Política de Privacidad</h1>
				<div class="updated-at">Actualizado al 05 de septiembre, 2017</div>
				<hr>
				<p>De conformidad a lo dispuesto en el artículo 19 Nº 4 de la Constitución Política de la República y la Ley Nº 19.628 se señala lo siguiente:</p>
				<p>El Ministerio Secretaría General de la Presidencia (“MINSEGPRES”), pone en conocimiento de todos quienes acceden al PORTAL CHILEATIENDE la siguiente Política de Privacidad, a fin de resguardar la seguridad, confidencialidad y privacidad del usuario y/o visitante de este sitio.</p>
				<p>Estas políticas tienen por finalidad asegurar la correcta utilización de la información recopilada a través de las visitas de este sitio y de sus contenidos.</p>
				<hr>
				<h2>I. Sobre los datos recopilados</h2>
				<p>MINSEGPRES recopila datos de los suscriptores, usuarios y/o visitantes que hagan uso de este portal, a través de dos mecanismos:</p>
				<ul>
					<li class="type-latin">
						<p><span>MECANISMOS AUTOMÁTICOS:</span> Son aquellos procesos informáticos realizados para generar registros de las actividades de los visitantes de sitios Web y cuyo objeto es establecer patrones de actividad, navegación y audiencia, que no implican la identificación personal de aquellos suscriptores, usuarios y/o visitantes que accedan al PORTAL CHILEATIENDE.</p>
						<p>MINSEGPRES se reserva el derecho de usar dicha información general, a fin de establecer criterios que mejoren los contenidos de este sistema, en todo caso siempre disociados de la persona que dejó los datos en su navegación.</p>
					</li>
					<li class="type-latin">
						<p><span>MECANISMOS MANUALES:</span> Son requerimientos formales y expresos de información a los suscriptores, usuarios y/o visitantes del portal que implican la recolección de datos personales tales como nombre, apellidos, domicilio, correo electrónico, ocupación, etc.</li>

						<p>MINSEGPRES recopila estos datos con el objeto de mejorar la calidad de información del portal y dar una mejor atención ante consultas de la ciudadanía.</p>
					</li>
				</ul>
				<hr>
				<h2>II. Sobre el tratamiento de los datos</h2>
				<ul>
					<li>
						<p>Respecto de la entrega de información recopilada por medio de los mecanismos automáticos antes señalados, y que no contengan identificación personal de los suscriptores, usuarios y/o visitantes de la página, esta podrá ser utilizada para informar a entidades públicas, gubernamentales o a terceros sobre patrones de actividad, navegación, audiencia y caracterización general de este sistema.</p>
					</li>
					<li>
						<p>Respecto de los datos personales de los usuarios, recolectados a través de mecanismos manuales u otros medios, éstos serán tratados con objeto de mejorar la calidad de información del portal y dar una mejor atención ante consultas de la ciudadanía.</p>
					</li>
				</ul>
				<p>Al aceptar las presentes Políticas de Privacidad, usted autoriza al MINSEGPRES para:</p>
				<ul>
					<li class="type-latin">
						<p>Comunicar a otros organismos del Estado u otros terceros sus datos personales con el único objeto de mejorar la calidad de información del portal y dar una mejor atención ante consultas de la ciudadanía.</p>
					</li>
					<li class="type-latin">
						<p>Comunicar a terceros información estadística elaborada a partir de los datos personales de sus usuarios, cuando de dichos datos no sea posible identificar individualmente a los titulares (datos disociados), de conformidad a la Ley.</p>
					</li>
				</ul>
				<p>Aparte de las autorizaciones establecidas anteriormente, MINSEGPRES mantendrá la debida confidencialidad de los datos personales y no los trasmitirá a terceros, salvo cuando se deban entregar en razón de un mandato legal o una orden emanada de los Tribunales de Justicia que así lo requiera.</p>
				<hr>
				<h2>III. Derechos del titular de los datos</h2>
				<p>En todo momento, el usuario podrá ejercer los derechos otorgados por la Ley Nº19.628 y sus modificaciones posteriores. En específico, podrá:</p>
				<ul>
					<li>
						<p>Solicitar información respecto de los bancos de datos de que sea responsable el organismo, el fundamento jurídico de su existencia, su finalidad, tipos de datos almacenados y descripción del universo de personas que comprende.</p>
					</li>
					<li>
						<p>Solicitar información sobre los datos relativos a su persona, su procedencia y destinatario, el propósito del almacenamiento y la individualización de las personas u organismos a los cuales sus datos son transmitidos regularmente.</p>
					</li>
					<li>
						<p>Solicitar que se modifiquen sus datos personales cuando ellos no sean correctos o no estén actualizados, si fuere procedente.</p>
					</li>
					<li>
						<p>Solicitar la eliminación o cancelación de los datos entregados cuando así lo desee, en tanto fuere procedente.</p>
					</li>
				</ul>
